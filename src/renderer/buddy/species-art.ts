export const HAT_ART: Record<Hat, string> = {
  none: '',
  crown: '   \\^^^/    ',
  tophat: '   [___]    ',
  propeller: '    -+-     ',
  halo: '   (   )    ',
  wizard: '    /^\\     ',
  beanie: '   (___)    ',
  tinyduck: '    ,>      ',
};

/** Blink frame — closes the eyes. Used occasionally in the animation. */
const BLINK_EYE = '-';

export function getArtFrame(species: Species, eye: Eye, frame: number = 0, blink: boolean = false): string[] {
  const frames = SPECIES_ART[species];
  const f = frames[frame % frames.length];
  const e = blink ? BLINK_EYE : eye;
  return f.map((line) => line.replace(/\{E\}/g, e));
}

export function applyHat(artLines: string[], hat: Hat): string[] {
  const hatLine = HAT_ART[hat];
  if (!hatLine) return artLines;
  const out = [...artLines];
  if (!out[0].trim()) out[0] = hatLine;
  return out;
}
